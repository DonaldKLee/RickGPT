function appendMessage(user, text, img) {
    const messagesContainer = document.getElementById('chat-messages');
    const messageDiv = document.createElement('div');
    messageDiv.className = 'message';
    const userImage = `<img src="${img}" alt="${user}" class="user-profile">`;
    const messageText = `<div class="message-info"><span class="user">${user}</span><p class="text">${text}</p></div>`;
    messageDiv.innerHTML = `${userImage}${messageText}`;
    messagesContainer.appendChild(messageDiv);
    messagesContainer.scrollTop = messagesContainer.scrollHeight;
    scrollChatMessagesToBottom(); // Scroll to bottom after adding message
}

function appendTypingMessage(user, text, img) {
    const messagesContainer = document.getElementById('chat-messages');
    const messageDiv = document.createElement('div');
    messageDiv.className = 'message';
    const userImage = `<img src="${img}" alt="${user}" class="user-profile">`;
    const typingIndicator = `<span class="typing-indicator"></span>`;
    const messageText = `<div class="message-info"><span class="user">${user}</span><p class="text"></p></div>`;
    messageDiv.innerHTML = `${userImage}${typingIndicator}${messageText}`;
    messagesContainer.appendChild(messageDiv);
    messagesContainer.scrollTop = messagesContainer.scrollHeight;

    simulateTypingEffect(messageDiv.querySelector('.text'), text);
}

function simulateTypingEffect(element, text) {
    const typingDelay = 1; // Adjust typing speed as needed
    let i = 0;

    function type() {
        if (i < text.length) {
            element.textContent += text.charAt(i);
            i++;
            scrollChatMessagesToBottom(); // Scroll to bottom after adding message

            setTimeout(type, typingDelay);
        }
    }

    type();
}

